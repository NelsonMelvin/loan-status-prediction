import streamlit as st
import pandas as pd
import joblib

# Load model (make sure 'loan_model.pkl' is in the same folder)
model = joblib.load('loan_model.pkl')
st.title("Load Status Prediction App")

# User input fields
gender = st.selectbox("Gender", ["Male", "Female"])
married = st.selectbox("Married", ["Yes", "No"])
dependents = st.selectbox("Number of Dependents", ["0", "1", "2", "3+"])
education = st.selectbox("Education", ["Graduate", "Not Graduate"])
self_employed = st.selectbox("Self Employed", ["Yes", "No"])
applicant_income = st.number_input("Applicant Income", value=0)
coapplicant_income = st.number_input("Coapplicant Income", value=0)
loan_amount = st.number_input("Loan Amount (in 1000s)", value=0)
loan_term = st.number_input("Loan Term (in months)", value=360)
credit_history = st.selectbox("Credit History", [1.0, 0.0])
property_area = st.selectbox("Property Area", ["Urban", "Semiurban", "Rural"])

# Encode input values
gender = 1 if gender == "Male" else 0
married = 1 if married == "Yes" else 0
education = 1 if education == "Graduate" else 0
self_employed = 1 if self_employed == "Yes" else 0
property_area_map = {"Rural": 0, "Semiurban": 1, "Urban": 2}
property_area = property_area_map[property_area]
dependents = 3 if dependents == "3+" else int(dependents)

# Collect input into DataFrame
input_data = pd.DataFrame([[
    gender, married, dependents, education, self_employed,
    applicant_income, coapplicant_income, loan_amount,
    loan_term, credit_history, property_area
]], columns=[
    'Gender', 'Married', 'Dependents', 'Education', 'Self_Employed',
    'ApplicantIncome', 'CoapplicantIncome', 'LoanAmount',
    'Loan_Amount_Term', 'Credit_History', 'Property_Area'
])

# Predict
if st.button("Predict Loan Approval"):
    prediction = model.predict(input_data)[0]
    st.success("Loan Approved" if prediction == 1 else "Loan Rejected")
